namespace FC.CI.App
{
    public static class CustomMath
    {
        public static int Somar(int n1, int nr2)
            => n1 + nr2;
        
        public static int Subtrair(int n1, int nr2)
            => n1 - nr2;
        
        public static int Multiplicar(int n1, int nr2)
            => n1 * nr2;
        
        public static int Dividir(int n1, int nr2)
            => n1 / nr2;
    }
}